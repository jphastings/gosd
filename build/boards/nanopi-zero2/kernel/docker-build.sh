#!/usr/bin/env bash
# Runs inside the debian:bookworm container started by build.sh. Not meant to
# be run directly on the host.
set -euo pipefail

: "${KERNEL_TAG:?KERNEL_TAG must be set}"
: "${KERNEL_REPO:?KERNEL_REPO must be set}"

DTB_PATH="arch/arm64/boot/dts/rockchip/rk3528-nanopi-zero2.dtb"
# The ARCH=arm64 dtb make target is resolved relative to
# arch/arm64/boot/dts/, so it must NOT be prefixed with that path (unlike
# DTB_PATH above, which is used for the cp step and needs the full path).
DTB_MAKE_TARGET="rockchip/rk3528-nanopi-zero2.dtb"
SRC_DIR="/build/linux"

apt-get update -qq
apt-get install -y -qq --no-install-recommends \
  build-essential crossbuild-essential-arm64 \
  git ca-certificates bc bison flex libssl-dev libelf-dev patch \
  >/dev/null

export ARCH=arm64
export CROSS_COMPILE=aarch64-linux-gnu-

echo "==> Cloning ${KERNEL_REPO} at ${KERNEL_TAG} (shallow)"
git clone --quiet --depth 1 --branch "${KERNEL_TAG}" "${KERNEL_REPO}" "${SRC_DIR}"
cd "${SRC_DIR}"

echo "==> Applying GoSD device-tree patches"
for patch in /work/patches/*.patch; do
  echo "    ${patch}"
  patch -p1 --forward <"${patch}"
done

echo "==> Base config: make defconfig"
make ARCH=arm64 defconfig

echo "==> Merging fragment: scripts/kconfig/merge_config.sh"
./scripts/kconfig/merge_config.sh -m .config /work/kernel-fragment.config

echo "==> Resolving dependencies: make olddefconfig"
make ARCH=arm64 CROSS_COMPILE="${CROSS_COMPILE}" olddefconfig

echo "==> Asserting required options survived olddefconfig"
required_y=(
  CONFIG_ARCH_ROCKCHIP
  CONFIG_MMC_DW
  CONFIG_MMC_DW_ROCKCHIP
  CONFIG_MMC_SDHCI_OF_DWCMSHC
  CONFIG_STMMAC_ETH
  CONFIG_DWMAC_ROCKCHIP
  CONFIG_REALTEK_PHY
  CONFIG_GPIO_ROCKCHIP
  CONFIG_I2C_RK3X
  CONFIG_SPI_ROCKCHIP
  CONFIG_SPI_SPIDEV
  CONFIG_SERIAL_8250_DW
)
missing=0
for opt in "${required_y[@]}"; do
  if ! grep -qx "${opt}=y" .config; then
    echo "FATAL: ${opt}=y did not survive olddefconfig" >&2
    missing=1
  fi
done
if ! grep -qx "# CONFIG_MODULES is not set" .config; then
  echo "FATAL: CONFIG_MODULES is set (modules must be disabled)" >&2
  missing=1
fi
if [ "${missing}" -ne 0 ]; then
  exit 1
fi

echo "==> Building Image"
make ARCH=arm64 CROSS_COMPILE="${CROSS_COMPILE}" -j"$(nproc)" Image

echo "==> Building ${DTB_MAKE_TARGET}"
make ARCH=arm64 CROSS_COMPILE="${CROSS_COMPILE}" -j"$(nproc)" "${DTB_MAKE_TARGET}"

echo "==> Copying outputs to /out"
{
  echo "# FriendlyElec NanoPi Zero2 (RK3528A) — full generated kernel config."
  echo "# Source: ${KERNEL_REPO} tag ${KERNEL_TAG}"
  echo "# Generated by: make ARCH=arm64 defconfig + scripts/kconfig/merge_config.sh"
  echo "#               -m kernel-fragment.config, then make olddefconfig."
  echo "# Do not hand-edit — regenerate via build.sh instead."
  cat .config
} > /out/kernel.config
cp arch/arm64/boot/Image /out/Image
cp "${DTB_PATH}" /out/rk3528-nanopi-zero2.dtb

echo "==> Done"
ls -la /out
